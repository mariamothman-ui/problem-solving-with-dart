import 'dart:io';

void main() {
  int a = int.parse(stdin.readLineSync()!);
  int b = int.parse(stdin.readLineSync()!);
  String c = stdin.readLineSync()!;
  double e = double.parse(stdin.readLineSync()!);
  double f = double.parse(stdin.readLineSync()!);
  print(a);
  print(b);
  print(c);
  print(e);
  print(f);
}
